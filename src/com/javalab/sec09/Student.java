package com.javalab.sec09;

public class Student {
	String id;
	String jumin;
	String name;
	int year;
	String address;
	int department;
	
	public Student() {
	}
	
	public Student(String id, String jumin, String name, int year, String address, int department) {
		this.id = id;
		this.jumin = jumin;
		this.name = name;
		this.year = year;
		this.address = address;
		this.department = department;
		
	}


}
